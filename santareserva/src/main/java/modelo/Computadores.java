package modelo;

public class Computadores {

	private int idPC;
	private int num;
	private int temp;
	
	public int getIdPC() {
		return idPC;
	}
	public void setIdPC(int idPC) {
		this.idPC = idPC;
	}
	public int getNum() {
		return num;
	}
	public void setNum(int num) {
		this.num = num;
	}
	public int getTemp() {
		return temp;
	}
	public void setTemp(int temp) {
		this.temp = temp;
	}
	
	

}
