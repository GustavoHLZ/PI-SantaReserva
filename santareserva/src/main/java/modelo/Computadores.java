package modelo;

public class Computadores {

	private int idPC;
	private int num;
	private int temp;
	private float preco;
	private Boolean disp;
	
	public float getPreco() {
		return preco;
	}
	public void setPreco(float preco) {
		this.preco = preco;
	}
	public Boolean getDisp() {
		return disp;
	}
	public void setDisp(Boolean disp) {
		this.disp = disp;
	}
	public int getIdPC() {
		return idPC;
	}
	public void setIdPC(int idPC) {
		this.idPC = idPC;
	}
	public int getNum() {
		return num;
	}
	public void setNum(int num) {
		this.num = num;
	}
	public int getTemp() {
		return temp;
	}
	public void setTemp(int temp) {
		this.temp = temp;
	}
	
	

}
