package modelo;

public class Sala_Reunioes {

	private int ID_Sala;
	private Boolean Disp_Sala;
	private int Temp_Alugado;
	private int Cap_Max;
	
	public int getID_Sala() {
		return ID_Sala;
	}
	public void setID_Sala(int iD_Sala) {
		ID_Sala = iD_Sala;
	}
	public Boolean getDisp_Sala() {
		return Disp_Sala;
	}
	public void setDisp_Sala(Boolean disp_Sala) {
		Disp_Sala = disp_Sala;
	}
	public int getTemp_Alugado() {
		return Temp_Alugado;
	}
	public void setTemp_Alugado(int temp_Alugado) {
		Temp_Alugado = temp_Alugado;
	}
	public int getCap_Max() {
		return Cap_Max;
	}
	public void setCap_Max(int cap_Max) {
		Cap_Max = cap_Max;
	}

}
